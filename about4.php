     	<div class="about-header"><b>What is SIMP?</b><br/></div>
	
	<p class="about-text">SIMP is a social media platform for schools that is intended to introduce students to the social media experience without inappropriate content or malicious individuals. SIMP is intended to be a safe social media environment where only students with registered school email domains can create accounts, post content, and interact with users. Currently, students sign up for social media platforms that are primarily intended for adults and are subsequently exposed to content that is not suited for them. Most of these platforms do not have any type of identity verification, allowing for anyone to create accounts and interact with the platform and all of its users. On these platforms, inappropriate content can easily be posted, viewed, and shared by millions of users before being taken down. By only allowing students with verified school email addresses on the platform and by monitoring their content and messages, SIMP can address these issues and provide students with a truly safe social media experience.</p>
