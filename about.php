<p>
     <br>&nbsp;&nbsp;Student International Media Platform (SIMP) is a social media platform for schools that serves the purpose to introduce the social media experience to the younger audience without exposing them to unwarranted and inappropriate content.
     <br>&nbsp;&nbsp;Most children in today's society are growing up in the digital age and most crave to be a part of social sedia experience.
     <br>&nbsp;&nbsp;SIMP provides a safe social media environment where only students with registered school email domains can create an account.  
     <br>&nbsp;&nbsp;This provides a more secure and monitored environment where users can post and share media without typical privacy and security concerns due to the lack of tracking and advertising. 
     <br>&nbsp;&nbsp;The biggest concern right now is there are so many students signing up to different social platforms that lie about their age in order to gain access to them. 
     <br>&nbsp;&nbsp;Most of these social platforms do not have any type of verification to check if the users are lying about their age or who they say they are. 
     <br>&nbsp;&nbsp;Having minors in a social media intended for adults can cause them to see content that is not made for them as well as be targeted by predators who have malicious intentions. 
     <br>&nbsp;&nbsp;The reason they are signing up for these social medias that are not intended for them, is because they want to be able to interact with friends and share content with one another. 
     <br>&nbsp;&nbsp;We can satisfy this desire by making a social media where they can post content and interact with others in a safe way.
</p>
        
